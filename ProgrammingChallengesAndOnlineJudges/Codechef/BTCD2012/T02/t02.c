#include <stdio.h>

int main(void)
{
	int t, n, k, x;
	scanf("%d", &t);
	while(t--)
	{
		scanf("%d", &n);
		scanf("%d", &k);
		k--;
		switch(n)
		{
			case 1:
				x = k + 1;
				break;
			case 2:
				x = (k + 1) * 11;
				break;
			case 3:
				x = (k / 10 + 1) * 101 + (k % 10) * 10;
				break;
			case 4:
				x = (k / 10 + 1) * 1001 + (k % 10) * 110;
				break;
			case 5:
				x = (k / 100 + 1) * 10001 + ((k / 10) % 10) * 1010 + (k % 10) * 100;
				break;
			case 6:
				x = (k / 100 + 1) * 100001 + ((k / 10) % 10) * 10010 + (k % 10) * 1100;
				break;
			case 7:
				x = (k / 1000 + 1) * 1000001 + ((k / 100) % 10) * 100010 + ((k / 10) % 10) * 10100 + (k % 10) * 1000;
				break;
			case 8:
				x = (k / 1000 + 1) * 10000001 + ((k / 100) % 10) * 1000010 + ((k / 10) % 10) * 100100 + (k % 10) * 11000;
				break;
			case 9:
				x = (k / 10000 + 1) * 100000001 + ((k / 1000) % 10) * 10000010 + ((k / 100) % 10) * 1000100 + ((k / 10) % 10) * 101000 + (k % 10) * 10000;
				break;
		}
		printf("%d\n", x);
	}
	return 0;
}

